  saveChanges() async {
    try {
      final userRepository = ref.read(userRepositoryProvider);

      setState(() {
        isLoading = false;
      });

      await userRepository.patchUser(
        phone: _model.textController1.text.trim(),
        name: _model.textController2.text.trim(),
        email: _model.textController3.text.trim(),
        city: _model.textController4.text.trim(),
      );

      ref.invalidate(userProvider);

      setState(() {
        isLoading = true;
      });

      if (mounted) context.goNamed(AppRoute.main.name);
    } catch (e) {
      if (mounted) context.goNamed(AppRoute.main.name);
    }
  }

